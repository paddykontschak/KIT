class AuthorPreferences {
    Color favColor;
    String favAnimal;
    String favBook;
    String favMovie;
    String favSong;

    public AuthorPreferences(Color color, String favAnimal, String favBook,
    		String favMovie, String favSong) {
    	this.favColor = color;
    	this.favAnimal = favAnimal;
    	this.favBook = favBook;
    	this.favMovie = favMovie;
    	this.favSong = favSong;
    }
}
